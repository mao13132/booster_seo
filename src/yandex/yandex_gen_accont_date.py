    @staticmethod
    def get_name_profile(name, surname):
        black_keyb = ["'", ',', '.', ' ']

        from transliterate import translit
        profile_name = translit(f"{name}{surname.lower()}", language_code='ru', reversed=True)

        for _black in black_keyb:
            if _black in profile_name:
                profile_name = profile_name.replace(_black, '')

        return profile_name

